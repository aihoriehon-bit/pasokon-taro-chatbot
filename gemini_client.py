def chat(user_message: str, history: list[dict] | None = None) -> str:
    """ユーザーメッセージに対してGeminiで応答を生成する。

    history: [{"role": "user"|"model", "content": "..."}] 形式の会話履歴
    """
    client = _get_client()
    if not client:
        return _fallback_answer(user_message)

    contents = []
    for msg in (history or []):
        role = "user" if msg.get("role") == "user" else "model"
        text = msg.get("content", "")
        if text:
            contents.append(types.Content(role=role, parts=[types.Part(text=text)]))
    contents.append(types.Content(role="user", parts=[types.Part(text=user_message)]))

    try:
        response = client.models.generate_content(
            model=MODEL_NAME,
            contents=contents,
            config=types.GenerateContentConfig(
                system_instruction=SYSTEM_PROMPT,
                temperature=0.7,
                max_output_tokens=1024,
            ),
        )
        reply = (response.text or "").strip()
        return _sanitize(reply) if reply else _fallback_answer(user_message)
    except Exception as e:
        print(f"Gemini API error: {e}")
        return _fallback_answer(user_message)


def _sanitize(text: str) -> str:
    """Gemini が指示を無視して出力した不要な要素を除去する。"""
    # 眼鏡の絵文字 👓 は使わない方針 (Geminiが時々入れてしまうため強制除去)
    text = text.replace("👓", "")
    # 連続スペースの整理
    text = text.replace("  ", " ")
    return text.strip()


